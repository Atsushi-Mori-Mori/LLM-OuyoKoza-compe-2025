import json
from pathlib import Path

src = Path("./data/spider.jsonl")
dst = Path("./data/spider_tool_as_user.jsonl")

tool_count = 0
total_msgs = 0
rows = 0

with src.open("r", encoding="utf-8") as f_in, dst.open("w", encoding="utf-8") as f_out:
    for line in f_in:
        if not line.strip():
            continue
        obj = json.loads(line)
        msgs = obj.get("messages", [])
        if not isinstance(msgs, list):
            raise ValueError("Invalid format: messages is not a list")

        for m in msgs:
            total_msgs += 1
            if m.get("role") == "tool":
                m["role"] = "user"
                tool_count += 1

        f_out.write(json.dumps(obj, ensure_ascii=False) + "\n")
        rows += 1

print("converted rows:", rows)
print("converted tool->user messages:", tool_count)
print("output:", dst)
