import json
import random

INPUT_PATH = "./data/sft_alfworld_v4.jsonl"      # 元データ
OUTPUT_PATH = "./data/sft_alfworld_v4_rebalanced.jsonl"  # 出力

# 100%残すタスク
KEEP_FULL = {
    "pick_two",
    "heat_and_place",
    "cool_and_place",
}

# 再現性確保
SEED = 3407
random.seed(SEED)


def iter_jsonl(path):
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            if line.strip():
                yield json.loads(line)


kept = 0
dropped = 0

with open(OUTPUT_PATH, "w", encoding="utf-8") as out_f:
    for ex in iter_jsonl(INPUT_PATH):

        md = ex.get("metadata") or {}
        task_type = md.get("task_type", "unknown")

        # 100%残すタスク
        if task_type in KEEP_FULL:
            out_f.write(json.dumps(ex, ensure_ascii=False) + "\n")
            kept += 1
            continue

        # その他は50%ランダム保持
        if random.random() < 0.5:
            out_f.write(json.dumps(ex, ensure_ascii=False) + "\n")
            kept += 1
        else:
            dropped += 1


print("==== Rebalance Summary ====")
print("Kept :", kept)
print("Dropped :", dropped)
print("Output saved to:", OUTPUT_PATH)
