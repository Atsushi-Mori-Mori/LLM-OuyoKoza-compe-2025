# download_spider_dataset.py
# Spider dataset の tables.json と database をローカルに取得する専用スクリプト

import os
from huggingface_hub import snapshot_download

LOCAL_DIR = "hf_spider_repo"   # 保存先フォルダ

def main():
    # すでに存在するか確認
    tables_path = os.path.join(LOCAL_DIR, "tables.json")
    db_dir = os.path.join(LOCAL_DIR, "database")

    if os.path.exists(tables_path) and os.path.isdir(db_dir):
        print(f"[INFO] Spider dataset already exists in '{LOCAL_DIR}'.")
        print("[INFO] Download skipped.")
        return

    print("[INFO] Downloading Spider dataset from Hugging Face...")
    repo_path = snapshot_download(
        repo_id="spider",
        repo_type="dataset",
        local_dir=LOCAL_DIR,
        local_dir_use_symlinks=False,
    )

    print(f"[DONE] Download completed.")
    print(f"[INFO] Saved to: {repo_path}")

if __name__ == "__main__":
    main()
